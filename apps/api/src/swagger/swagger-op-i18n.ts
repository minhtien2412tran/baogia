function patternLocalize(lang: SwaggerLang, summary: string): string {
  if (lang === 'en') return summary;
  const rules: Array<[RegExp, string]> =
    lang === 'vi'
      ? [
          [/^List /i, 'Danh sách '],
          [/^Create /i, 'Tạo '],
          [/^Get /i, 'Lấy '],
          [/^Update /i, 'Cập nhật '],
          [/^Delete /i, 'Xóa '],
          [/^Approve /i, 'Duyệt '],
          [/^Reject /i, 'Từ chối '],
          [/^Submit /i, 'Gửi '],
          [/^Search /i, 'Tìm '],
          [/^Send /i, 'Gửi '],
          [/^Upload /i, 'Tải lên '],
          [/^Cancel /i, 'Hủy '],
          [/^Request /i, 'Yêu cầu '],
          [/^Subscribe /i, 'Đăng ký '],
        ]
      : [
          [/^List /i, '列出'],
          [/^Create /i, '创建'],
          [/^Get /i, '获取'],
          [/^Update /i, '更新'],
          [/^Delete /i, '删除'],
          [/^Approve /i, '批准'],
          [/^Reject /i, '拒绝'],
          [/^Submit /i, '提交'],
          [/^Search /i, '搜索'],
          [/^Send /i, '发送'],
          [/^Upload /i, '上传'],
          [/^Cancel /i, '取消'],
          [/^Request /i, '申请'],
          [/^Subscribe /i, '订阅'],
        ];
  let out = summary;
  for (const [re, rep] of rules) {
    if (re.test(out)) {
      out = out.replace(re, rep);
      break;
    }
  }
  return out;
}

export function localizeOperationSummary(lang: SwaggerLang, summary: string): string {
  const key = String(summary || '').trim();
  if (!key || lang === 'en') return key;
  const map = lang === 'vi' ? SUMMARY_VI : SUMMARY_ZH;
  return map[key] || patternLocalize(lang, key);
}
