export function tn(webLocale: string, key: string): string {
  const db = toDbLocale(webLocale);
  const catalog = NAV_BY_DB[db] ?? en;
  return catalog[key] ?? en[key] ?? key;
}

export type ResolvedNavLink = { href: string; label: string; description?: string };
export type ResolvedNavGroup = { title: string; links: ResolvedNavLink[] };

function resolveLinks(webLocale: string, defs: NavLinkDef[]): ResolvedNavLink[] {
  return defs.map((d) => ({
    href: d.href,
    label: tn(webLocale, d.labelKey),
    description: d.descKey ? tn(webLocale, d.descKey) : undefined,
  }));
}

export function getMegaMenu(webLocale: string): ResolvedNavGroup[] {
  return MEGA_MENU_DEF.map((g) => ({
    title: tn(webLocale, g.titleKey),
    links: resolveLinks(webLocale, g.links),
  }));
}

export function getFooterServices(webLocale: string): ResolvedNavLink[] {
  return resolveLinks(webLocale, FOOTER_SERVICES_DEF);
}

export function getFooterCompany(webLocale: string): ResolvedNavLink[] {
  return resolveLinks(webLocale, FOOTER_COMPANY_DEF);
}

export function getQuickLinks(webLocale: string): ResolvedNavLink[] {
  return resolveLinks(webLocale, QUICK_LINKS_DEF);
}
